#!/bin/bash
set -e

psql -v ON_ERROR_STOP=1 --username "$POSTGRES_USER" --dbname "$POSTGRES_DB" <<-EOSQL
	CREATE USER $POSTGRES_ORDERS_USER WITH PASSWORD '$POSTGRES_CUSTOMERS_PASSWORD';
	CREATE DATABASE $POSTGRES_ORDERS_DB;
	GRANT ALL PRIVILEGES ON DATABASE $POSTGRES_ORDERS_DB TO $POSTGRES_CUSTOMERS_USER;
EOSQL
